from tkinter import *





#__vvvvvvvvvvv_configuraçao de uma janela_vvvvvvvv_____
janela = Tk()
janela.title("mauricio")
janela.geometry("400x400")
texto = Label(janela, text="bem-vindo")
texto.grid(column=2, row=0, padx=12, pady=12)



janela.mainloop()
